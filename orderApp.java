    private void setAmount(){
        this.amount = 0;
        if(jCheckBoxPizza.isSelected()){
            this.amount += 30000;
        }
        if(jCheckBoxSpagetti.isSelected()){
            this.amount += 20000;
        }
        if(jCheckBoxSteak.isSelected()){
            this.amount += 35000;
        }
    }
    
    private String getAmount(){
        setAmount();
        String _amount = Integer.toString(this.amount);
        return _amount;
    }
    
    private static void appendUsingFileWriter(String fileName, String text) {
        File file = new File(fileName);
        FileWriter fr = null;
        try {
                // Below constructor argument decides whether to append or override
                fr = new FileWriter(file, true);
                fr.write(text);

        } catch (IOException e) {
                e.printStackTrace();
        } finally {
                try {
                        fr.close();
                } catch (IOException e) {
                        e.printStackTrace();
                }
        }
    }
    
    
