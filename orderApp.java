    private void setPesanan(){
        if(jCheckBoxPizza.isSelected()){
            this.customer += ">  Pizza\t   Rp.  30000\n";
        }
        if(jCheckBoxSpagetti.isSelected()){
            this.customer += ">  Spagetti\t   Rp.  20000\n";
        }
        if(jCheckBoxSteak.isSelected()){
            this.customer += ">  Steak \t   Rp.  35000\n";
        }
    }
    
